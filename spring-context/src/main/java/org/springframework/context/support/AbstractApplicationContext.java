	@Override
	public void refresh() throws BeansException, IllegalStateException {

		synchronized (this.startupShutdownMonitor) {
			// 1、初始化之前的准备，处理配置文件的占位符以及校验配置文件相关属性、清理容器上下文中的缓存
			// 记录容器的启动时间、标记“已启动”状态、检查环境变量
			prepareRefresh();

			// 2、这一步是将配置文件中信息读取并解析为一个个BeanDefinition的bean定义信息，将bean定义信息注册到BeanFactory中
			// 注：这里bean还没有初始化，只是被将其定义信息提取出来了，注册也只是将这些信息都保存到了注册中心(本质是beanName-> beanDefinition的map)
			// 初始化BeanFactory容器、注册BeanDefinition
			ConfigurableListableBeanFactory beanFactory = obtainFreshBeanFactory();

			// 3、设置应用上下文属性（上下文类加载器和 BeanPostProcessor 处理器），手工注册了几个特殊的bean
			// a、这里面会创建一个bean后置处理器 ApplicationContextAwareProcessor，
			// 	ApplicationContextAwareProcessor 的 postProcessBeforeInitialization 方法会调用 invokeAwareInterfaces 方法，
			// 	在该方法中调用 EnvironmentAware/EmbeddedValueResolverAware/ResourceLoaderAware/ApplicationEventPublisherAware/MessageSourceAware/ApplicationContextAware 接口的方法
			//  ApplicationEventPublisherAware 注册自定义广播器
			// b、创建bean后置处理器 ApplicationListenerDetector，会在 postProcessAfterInitialization 中注册监听器
			prepareBeanFactory(beanFactory);

			try {
				// 4、这里是提供给子类的扩展点，到这里，所有的 Bean 都加载并注册完成了，但是都还没有初始化（在这步的时候添加一些特殊的 BeanFactoryPostProcessor 的实现类或做点什么事）
				postProcessBeanFactory(beanFactory);

				// 5、调用 BeanFactoryPostProcessor 各个实现类的 postProcessBeanFactory(factory) 方法
				// BeanFactoryPostProcessor 为spring在容器初始化时对外对外暴露的扩展点，
				// Spring IoC容器允许 BeanFactoryPostProcessor 在容器加载注册 BeanDefinition 完成之后读取 BeanDefinition (配置元数据)，并可以修改它
				//////// springboot流程中，此流程会解析主类的各项注解
				invokeBeanFactoryPostProcessors(beanFactory);

				// 6、注册 BeanPostProcessor 的实现类，(注意和 BeanFactoryPostProcessor 的区别)
				// 注册到容器, 调用 AbstractBeanFactory.addBeanPostProcessor
				// 此接口两个方法: postProcessBeforeInitialization 和 postProcessAfterInitialization。两个方法分别在 Bean 初始化之前和初始化之后得到执行。注意，到这里 Bean 还没初始化
				// 在 AbstractAutowireCapableBeanFactory.applyBeanPostProcessorsAfterInitialization 方法中执行
				registerBeanPostProcessors(beanFactory);

				// 7、国际化消息的初始化
				initMessageSource();

				// 8、初始化当前应用上下文中的事件广播器
				initApplicationEventMulticaster();

				// 9、这里是一个回调方法，留给子类去实现。可以在这里初始化一些特殊的 Bean（在初始化 singleton beans 之前）
				onRefresh();

				// 10、注册事件监听器，监听器需要实现 ApplicationListener 接口。
				registerListeners();

				// 11、初始化所有的 singleton beans（lazy-init的除外）
				// 此处会调用第 6 步中注册的 BeanPostProcessor 实现类
				// 以及 BeanNameAware 等几个 Aware 子接口（AbstractAutowireCapableBeanFactory.initializeBean()-->invokeAwareMethods()）
				finishBeanFactoryInitialization(beanFactory);

				// 12、发布或广播相应的事件
				finishRefresh();

			} catch (BeansException ex) {
				if (logger.isWarnEnabled()) {
					logger.warn("Exception encountered during context initialization - cancelling refresh attempt: " + ex);
				}
				// 销毁已经创建的单例对象
				destroyBeans();
				//重新设置BeanFactory的激活属性active的值
				cancelRefresh(ex);
				throw ex;
			} finally {
				resetCommonCaches();
			}
		}
	}

	/**
	 * 准备工作，标记“已启动”，设置启动时间，处理配置文件中的占位符，容器中相关缓存的清理和初始化
	 */
	protected void prepareRefresh() {
		this.startupDate = System.currentTimeMillis();
		this.closed.set(false);
		this.active.set(true);

		if (logger.isInfoEnabled()) {
			logger.info("Refreshing " + this);
		}
		// 处理配置文件中的占位符
		initPropertySources();
		// 校验配置属性
		getEnvironment().validateRequiredProperties();

		if (this.earlyApplicationListeners == null) {
			this.earlyApplicationListeners = new LinkedHashSet<>(this.applicationListeners);
		} else {
			this.applicationListeners.clear();
			this.applicationListeners.addAll(this.earlyApplicationListeners);
		}
		this.earlyApplicationEvents = new LinkedHashSet<>();
	}

